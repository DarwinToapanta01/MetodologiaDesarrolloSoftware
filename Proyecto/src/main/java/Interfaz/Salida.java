    private void eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarActionPerformed
        int filaSeleccionada = mostrarInfo.getSelectedRow();

        // Verificar si se ha seleccionado alguna fila
        if (filaSeleccionada != -1) {
            // Obtener el valor del ID de la fila seleccionada en la columna 0
            int id = Integer.parseInt(mostrarInfo.getValueAt(filaSeleccionada, 0).toString());

            // Llamar al mÃ©todo Eliminar pasando el ID obtenido
            usuario.Eliminar(id);
        } else {
            JOptionPane.showMessageDialog(this, "Selecciona una fila para eliminar");
        }
    }//GEN-LAST:event_eliminarActionPerformed

