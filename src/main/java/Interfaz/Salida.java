    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed
        // TODO add your handling code here:
         // Obtener el ID ingresado en el campo de texto
    String idStr = ingresoId.getText();

    // Verificar si se ha ingresado un ID
    if (!idStr.isEmpty()) {
        try {
            // Convertir el ID a entero
            int id = Integer.parseInt(idStr);
            
            // Buscar y mostrar la informaciÃ³n del usuario por el ID
            usuario.buscarUsuarioPorId(id, mostrarInfo);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese un ID vÃ¡lido.");
        }
    } else {
        JOptionPane.showMessageDialog(this, "Por favor, ingrese un ID.");
    }
    ingresoId.setText("");
    }//GEN-LAST:event_buscarActionPerformed

